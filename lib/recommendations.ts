export function getRecommendationsForMonth(
  month: number,
  activeFeatures: FeatureKey[],
  climateZone: string,
  dismissedIds: string[] = []
): Recommendation[] {
  const priorityOrder = { critical: 0, high: 1, medium: 2, low: 3 };

  return ACTIONS
    .filter((a) => {
      if (!activeFeatures.includes(a.feature)) return false;
      if (!a.months.includes(month)) return false;
      if (dismissedIds.includes(a.id)) return false;
      if (a.zones !== 'all' && !a.zones.includes(climateZone)) return false;
      return true;
    })
    .sort((a, b) => priorityOrder[a.priority] - priorityOrder[b.priority]);
}

export function getAgeAlerts(
  features: Record<string, { active: boolean; installYear: number | null }>,
  currentYear: number = new Date().getFullYear()
): Array<{ feature: string; featureName: string; emoji: string; age: number; msg: string; priority: string }> {
  const alerts: Array<{ feature: string; featureName: string; emoji: string; age: number; msg: string; priority: string }> = [];

  Object.entries(features).forEach(([key, feat]) => {
    if (!feat.active || !feat.installYear) return;
    const age = currentYear - feat.installYear;
    const featureAlerts = AGE_ALERTS[key] || [];
    const meta = FEATURE_META[key as FeatureKey];
    if (!meta) return;

    featureAlerts.forEach((a) => {
      if (age >= a.years) {
        alerts.push({
          feature: key,
          featureName: meta.name,
          emoji: meta.emoji,
          age,
          msg: a.msg,
          priority: a.priority,
        });
      }
    });
  });

  return alerts;
}

export function buildTaskFromRecommendation(rec: Recommendation) {
  const category = FEATURE_CATEGORY_MAP[rec.feature] || CATEGORY_MAP[rec.category] || 'Exterior';

  // Calculate due date — set to 7 days from now for this month's tasks
  const dueDate = new Date();
  dueDate.setDate(dueDate.getDate() + 7);

  return {
    name: rec.title,
    category,
    due_date: dueDate.toISOString().split('T')[0],
    recurrence: rec.recurrenceType,
    recurrence_days: rec.recurrenceDays || null,
    notes: `${rec.desc}\n\nEstimated cost: $${rec.costEstLow}${rec.costEstHigh > rec.costEstLow ? '–$' + rec.costEstHigh : ''} | ${rec.diyPro} | ${rec.recurring}`,
    estimated_time: rec.timeEstMinutes,
    estimated_cost: rec.costEstHigh > 0 ? rec.costEstHigh : null,
    priority: rec.priority === 'critical' ? 1 : rec.priority === 'high' ? 2 : rec.priority === 'medium' ? 3 : 4,
  };
}
