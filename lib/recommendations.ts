export interface Recommendation {
  id: string;
  feature: FeatureKey;
  title: string;
  desc: string;
  priority: 'critical' | 'high' | 'medium' | 'low';
  category: string;       // maps to your existing task categories
  months: number[];        // 0-indexed (0 = Jan, 11 = Dec)
  recurring: string;       // display label
  recurrenceType: 'one_time' | 'monthly' | 'quarterly' | 'yearly' | 'custom';
  recurrenceDays?: number; // for 'custom' type
  timeEstMinutes: number;  // estimated time in minutes
  costEstLow: number;      // DIY cost
  costEstHigh: number;     // pro cost
  diyPro: 'DIY' | 'Pro' | 'Either';
  zones: string[] | 'all'; // climate zones this applies to
}

export type FeatureKey =
  | 'hvac' | 'gutters' | 'waterHeater' | 'garage' | 'fireplace'
  | 'dryer' | 'pool' | 'roof' | 'smokeDetectors' | 'lawn'
  | 'sumpPump' | 'irrigation' | 'deck' | 'waterSoftener'
  | 'generator' | 'septic';

export interface AgeAlert {
  years: number;
  msg: string;
  priority: 'critical' | 'high' | 'medium';
}

